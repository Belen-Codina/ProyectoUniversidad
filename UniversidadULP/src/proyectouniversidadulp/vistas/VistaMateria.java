    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
         String nombre=jTNombre.getText();        
        //int id=Integer.parseInt(jTId.getText());
        int cuatrimestre= parseInt(jTCuatrimestre.getText());
        boolean activo = jChBActivo.isEnabled();
        
        Materia materia = new Materia (nombre, cuatrimestre, activo);
       materiaData.guardarMateria(materia);
        
        jTId.setText(materia.getId_materia()+"");
        
    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(jTId.getText());
        Materia materia = materiaData.buscarMateria(id);
        if(materia != null){
           
            jTNombre.setText(materia.getNombre());
            jTCuatrimestre.setText(materia.getCuatrimestre()+"");
            jChBActivo.setEnabled(materia.isActivo());}
    }//GEN-LAST:event_jBBuscarActionPerformed

